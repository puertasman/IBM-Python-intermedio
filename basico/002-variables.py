# creación de variables

nombre = 'Enrique'
edad = 44
pais = 'España'

# mostramos información en pantalla
print('Nombre: ', nombre)
print('Edad: ', edad)
print('País: ', pais)

# modificamos los datos
nombre = 'José'
edad = 40

print('Nombre: ', nombre)
print('Edad: ', edad)
print('País: ', pais)


# son tipos de datos no estáticos
edad = 'cuarenta' 

print('Nombre: ', nombre)
print('Edad: ', edad)
print('País: ', pais)

